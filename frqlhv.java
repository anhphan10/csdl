    public void selecttionChanged(){
        int row = tb.getSelectedRow();
        if(row >= 0){
            String mahv = tb.getValueAt(row, 0).toString();
            String hoten = tb.getValueAt(row, 1).toString();
            String quequan = tb.getValueAt(row, 2).toString();
            String diemthi = tb.getValueAt(row, 3).toString();
            
            txtMaHV.setText(mahv);
            txtHoTen.setText(hoten);
            txtQueQuan.setText(quequan);
            txtDiemThi.setText(diemthi);
        }
    }
