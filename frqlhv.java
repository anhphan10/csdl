     private void loadTable(ResultSet kq){
        Object []hang = new Object[4];
        DefaultTableModel model = (DefaultTableModel) tb.getModel();
        model.setRowCount(0);
        try{
            while(kq.next()){
                hang[0] = kq.getString(1);
                hang[1] = kq.getString(2);
                hang[2] = kq.getString(3);
                hang[3] = kq.getString(4);
                model.addRow(hang);
                
            }
            
        }catch(Exception e){
            System.out.println("Loi" + e.toString());
        }
    }

